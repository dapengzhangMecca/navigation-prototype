const getMockedData = (data = []) => {
  var result = []

  for (let i = 0; i < data.length; i += 1) {
    let item = data[i]
    const { level1, level2, level3 } = item
    const level1Item = result.find(item => item.name === level1)
    if (level1 && level1Item) {
      const level2Item = level1Item.subCategories.find(
        item => item.name === level2,
      )

      if (level2 && level2Item) {
        level2Item.subCategories.push({
          name: level3,
          subCategories: [],
          url: '',
          parent: level2Item,
        })
      } else {
        const newItem = {
          name: level2,
          subCategories: [],
          url: '',
          parent: level1Item,
        }

        if (level3) {
          newItem.subCategories.push({
            name: level3,
            subCategories: [],
            url: '',
            parent: newItem,
          })
        }
        level1Item.subCategories.push(newItem)
      }
    } else {
      const newItem = {
        name: level1,
        subCategories: [],
        url: '',
      }

      if (level2) {
        const newSubItem = {
          name: level2,
          subCategories: [],
          url: '',
          parent: newItem,
        }

        if (level3) {
          const newSubSubItem = {
            name: level3,
            subCategories: [],
            url: '',
            parent: newSubItem,
          }

          newSubItem.subCategories.push(newSubSubItem)
        }

        newItem.subCategories.push(newSubItem)
      }

      result.push(newItem)
    }
  }

  return result
}

